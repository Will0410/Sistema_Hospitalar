package Model;

public class product {
    public String id_produt;
    public String nome_produt;

    public String valor_produt;


    public String getId_produt() {
        return id_produt;
    }

    public void setId_produt(String id_produt) {
        this.id_produt = id_produt;
    }

    public String getNome_produt() {
        return nome_produt;
    }

    public void setNome_produt(String nome_produt) {
        this.nome_produt = nome_produt;
    }

    public String getValor_produt() {
        return valor_produt;
    }

    public void setValor_produt(String valor_produt) {
        this.valor_produt = valor_produt;
    }


}
